class TaskReport {
  String taskType;
  String status;
  String taskName;
  String taskDetail;
  String taskLocation;
  String taskDate;
  String startTime;
  String endTime;
  String reportNote;

  TaskReport({
    required this.taskType,
    required this.status,
    required this.taskName,
    required this.taskDetail,
    required this.taskLocation,
    required this.taskDate,
    required this.startTime,
    required this.endTime,
    required this.reportNote,
  });
}
